#ifndef SFU_ANIMATION_HPP
#define SFU_ANIMATION_HPP

#include <SFML/System/Vector2.hpp>
#include <SFML/Graphics/Rect.hpp>

namespace sfu
{

    ////////////////////////////////////////////////////////////
    /// \brief The Animation class allow to create animated
    /// sprites easily
    ///
    ////////////////////////////////////////////////////////////
    class Animation
    {
    public:
        
        ////////////////////////////////////////////////////////////
        /// \brief Constructor
        ///
        ////////////////////////////////////////////////////////////
        Animation();
        
        ////////////////////////////////////////////////////////////
        /// \brief Contructor
        ///
        ////////////////////////////////////////////////////////////
        Animation(sf::Vector2i AnimationStart, sf::Vector2i FrameSize,
                  unsigned int FramesNum, float Speed);
        
        ////////////////////////////////////////////////////////////
        /// \brief Set the animation start position
        ///
        ////////////////////////////////////////////////////////////
        void SetStartPosition(const sf::Vector2i StartPosition);
        
        ////////////////////////////////////////////////////////////
        /// \brief Set the animation frame size
        ///
        ////////////////////////////////////////////////////////////
        void SetFrameSize(const sf::Vector2i FrameSize);
        
        ////////////////////////////////////////////////////////////
        /// \brief Set the number of frames
        ///
        ////////////////////////////////////////////////////////////
        void SetNumFrames(const unsigned int FrameCount);
        
        ////////////////////////////////////////////////////////////
        /// \brief Set the animation speed
        ///
        ////////////////////////////////////////////////////////////
        void SetSpeed(const float speed);
        
        ////////////////////////////////////////////////////////////
        /// \brief Get the animation start position
        ///
        ////////////////////////////////////////////////////////////
        const sf::Vector2i GetStartPosition() const;
        
        ////////////////////////////////////////////////////////////
        /// \brief Get the animation frame size
        ///
        ////////////////////////////////////////////////////////////
        const sf::Vector2i GetFrameSize() const;
        
        ////////////////////////////////////////////////////////////
        /// \brief Get the animation number of frames
        ///
        ////////////////////////////////////////////////////////////
        const unsigned int GetNumFrames() const;
        
        ////////////////////////////////////////////////////////////
        /// \brief Get the animation speed
        ///
        ////////////////////////////////////////////////////////////
        const float GetSpeed() const;
        
        ////////////////////////////////////////////////////////////
        /// \brief Update
        ///
        ////////////////////////////////////////////////////////////
        void Update(const float etime);
        
        ////////////////////////////////////////////////////////////
        /// \brief Get the current frame of the texture
        ///
        ////////////////////////////////////////////////////////////
        sf::IntRect GetFrame() const;
        
    private:
        
        sf::Vector2i m_startpos;
        sf::Vector2i m_frame_size;
        float m_speed;
        unsigned int m_frames_num;
        unsigned int m_frame_current;
        float m_time;
        
    };
    
}

#endif //SFU_ANIMATION_HPP